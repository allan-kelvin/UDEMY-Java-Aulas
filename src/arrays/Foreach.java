/*
 * Criando um Foreach
 */
package arrays;

public class Foreach {
    public static void main(String[] args) {
        double numeros[] ={10,9.0,8.0};
        
        //Criando um Foreach
        
        for (double numero : numeros) {
                System.out.println(numero);
            
        }
    }
    
}
